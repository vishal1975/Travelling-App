package com.riseinsteps.packbagbuddy.model;

public class Review {
    String id;
    String comment;

    public Review(){

    }
    public Review(String id, String comment) {
        this.id = id;
        this.comment = comment;
    }

    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }

    public String getComment() {
        return comment;
    }

    public void setComment(String comment) {
        this.comment = comment;
    }
}
